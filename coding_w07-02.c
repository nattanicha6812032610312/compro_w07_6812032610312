#include <stdio.h>  // เรียกใช้ฟังก์ชันพื้นฐาน เช่น printf, scanf

int main() {
    int x, y;  // ประกาศตัวแปรสำหรับเก็บค่าที่รับมาจากผู้ใช้

    // รับค่าจากผู้ใช้
    printf("ป้อนตัวเลข x: ");
    scanf("%d", &x);  // รับค่าและเก็บไว้ในตัวแปร x

    printf("ป้อนตัวเลข y: ");
    scanf("%d", &y);  // รับค่าและเก็บไว้ในตัวแปร y

    // เปรียบเทียบค่าระหว่าง x กับ y
    if (x > y) {
        printf("x มากกว่า y\n");  // กรณีที่ x มากกว่า y
    } else if (x < y) {
        printf("x น้อยกว่า y\n");  // กรณีที่ x น้อยกว่า y
    } else {
        printf("x เท่ากับ y\n");  // กรณีที่ x เท่ากับ y
    }

    return 0;  // จบโปรแกรม
}